    // Specify validation rules
    rules: {
      // The key name on the left side is the name attribute
      // of an input field. Validation rules are defined
      // on the right side
      fname: "required",
	email: {
        required: true,
        // Specify that email should be validated
        // by the built-in "email" rule
        email: true
      },
      phone: {
	required: true,
	regex: "^[0-9 \\-\\+]{4,17}$"

      },
      message: "required"
    },
    // Specify validation error messages
    messages: {
      fname: "Please enter your Name",
      phone: "Please enter a valid Phone No",
      message: "Please enter your Message",
      email: "Please enter a valid Email address"
    },
    // Make sure the form is submitted to the destination defined
    // in the "action" attribute of the form when valid
    submitHandler: function(form) {
	$('.submit-loading-img').show();
	$('#contact_us').prop('disabled', true);
    	//Ajax code here
	$.ajax({
        	url: form.action,
		dataType: "json",
            	type: form.method,
            	data: $(form).serialize(),
            	success: function(e) {
			$('.submit-loading-img').hide(); 
			$('#contact_us').prop('disabled', false);
			if(e.code == true){
                		$('.success-msg').fadeIn().html(e.success);
				$("#contactForm")[0].reset();
				setTimeout(function() {
				$('.success-msg').fadeOut("slow");
				}, 5000 );
			}
			else{
				$('.err-msg').fadeIn().html(e.err);
				setTimeout(function() {
				$('.err-msg').fadeOut("slow");
				}, 5000 );
			}
			
            	},
		error: function(){}             
        });
		
